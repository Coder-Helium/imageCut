from __future__ import annotations

import sys
from pathlib import Path
from typing import Any, Dict, List

from .geometry import BBox, fit_box_to_aspect, parse_aspect_ratio
from .schema import Candidate, Detection


def generate_existing_rule_candidates(
    image_w: int,
    image_h: int,
    target_aspect: str,
    detections: List[Detection],
    semantic_info: Dict[str, Any],
    caption_rule_root: str = "caption-rule-co",
    max_num: int = 12,
) -> List[Candidate]:
    """Call the existing caption-rule-co semantic dispatcher if available."""
    root = Path(caption_rule_root).resolve()
    if not root.exists():
        return []
    if str(root) not in sys.path:
        sys.path.insert(0, str(root))

    try:
        from rules.rule_semantic_dispatcher import generate_candidates_by_semantic
    except Exception:
        return []

    det_json = [
        {
            "class": d.name,
            "bbox": d.bbox.to_xyxy_int(),
            "conf": d.confidence,
        }
        for d in detections
    ]

    try:
        boxes = generate_candidates_by_semantic(
            W=image_w,
            H=image_h,
            detections=det_json,
            semantic_info=semantic_info,
            max_num=max_num,
        )
    except Exception:
        return []

    aspect = parse_aspect_ratio(target_aspect, image_w, image_h)
    out: List[Candidate] = []
    for idx, box in enumerate(boxes):
        converted = _convert_box(box)
        if converted is None:
            continue
        converted = fit_box_to_aspect(converted, aspect, image_w, image_h, padding=0.0)
        out.append(
            Candidate(
                candidate_id=f"existing_rule_{idx:04d}",
                box=converted,
                source="existing_semantic_rule",
                action="semantic_rule",
                issue=semantic_info.get("semantic_type", "unknown"),
                reason="Generated by existing caption-rule-co semantic rules.",
            )
        )
    return out


def _convert_box(box: Any) -> BBox | None:
    if box is None:
        return None
    if hasattr(box, "to_int_xyxy"):
        return BBox.from_seq(box.to_int_xyxy())
    if all(hasattr(box, k) for k in ["x1", "y1", "x2", "y2"]):
        return BBox(float(box.x1), float(box.y1), float(box.x2), float(box.y2))
    if isinstance(box, (list, tuple)) and len(box) == 4:
        return BBox.from_seq(box)
    return None
